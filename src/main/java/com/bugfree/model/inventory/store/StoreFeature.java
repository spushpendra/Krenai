package com.bugfree.model.inventory.store;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="store_feature")
public class StoreFeature {

	@Id
	@GeneratedValue
	private int storeFeatureId;
	
	private String storeFeature;
	
	private boolean freedelivery;
	private boolean quickdelivery;
	private boolean ondemanddelivery;
	private boolean nightdelivery;
	private boolean twenty4by7online;
	private boolean freshnessguaranteed;
	private boolean hundredbranded;
	private boolean bestquality;
	private boolean creditfacility;
	private boolean cashondelivery;
	private boolean onlycash;
	private boolean bestprice;
	
	public int getStoreFeatureId() {
		return storeFeatureId;
	}

	public void setStoreFeatureId(int storeFeatureId) {
		this.storeFeatureId = storeFeatureId;
	}

	public String getStoreFeature() {
		return storeFeature;
	}

	public void setStoreFeature(String storeFeature) {
		this.storeFeature = storeFeature;
	}

	public boolean isFreedelivery() {
		return freedelivery;
	}

	public void setFreedelivery(boolean freedelivery) {
		this.freedelivery = freedelivery;
	}

	public boolean isQuickdelivery() {
		return quickdelivery;
	}

	public void setQuickdelivery(boolean quickdelivery) {
		this.quickdelivery = quickdelivery;
	}

	public boolean isOndemanddelivery() {
		return ondemanddelivery;
	}

	public void setOndemanddelivery(boolean ondemanddelivery) {
		this.ondemanddelivery = ondemanddelivery;
	}

	public boolean isNightdelivery() {
		return nightdelivery;
	}

	public void setNightdelivery(boolean nightdelivery) {
		this.nightdelivery = nightdelivery;
	}

	public boolean isTwenty4by7online() {
		return twenty4by7online;
	}

	public void setTwenty4by7online(boolean twenty4by7online) {
		this.twenty4by7online = twenty4by7online;
	}

	public boolean isFreshnessguaranteed() {
		return freshnessguaranteed;
	}

	public void setFreshnessguaranteed(boolean freshnessguaranteed) {
		this.freshnessguaranteed = freshnessguaranteed;
	}

	public boolean isHundredbranded() {
		return hundredbranded;
	}

	public void setHundredbranded(boolean hundredbranded) {
		this.hundredbranded = hundredbranded;
	}

	public boolean isBestquality() {
		return bestquality;
	}

	public void setBestquality(boolean bestquality) {
		this.bestquality = bestquality;
	}

	public boolean isCreditfacility() {
		return creditfacility;
	}

	public void setCreditfacility(boolean creditfacility) {
		this.creditfacility = creditfacility;
	}

	public boolean isCashondelivery() {
		return cashondelivery;
	}

	public void setCashondelivery(boolean cashondelivery) {
		this.cashondelivery = cashondelivery;
	}

	public boolean isOnlycash() {
		return onlycash;
	}

	public void setOnlycash(boolean onlycash) {
		this.onlycash = onlycash;
	}

	public boolean isBestprice() {
		return bestprice;
	}

	public void setBestprice(boolean bestprice) {
		this.bestprice = bestprice;
	}
	
	
	
}
